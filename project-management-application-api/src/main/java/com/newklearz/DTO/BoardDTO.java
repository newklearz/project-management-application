package com.newklearz.DTO;

public class BoardDTO
{
    private Integer id;
    private String name;

    public BoardDTO()
    {
    }

    public Integer getId()
    {
        return id;
    }

    public void setId(Integer id)
    {
        this.id = id;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }
}